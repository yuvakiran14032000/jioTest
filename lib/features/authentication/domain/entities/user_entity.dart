class UserEntity {
  final String id;
  final String fullname;
  final String phone;
  final String email;
  final String password;
  final String profile;
  final String usertype;

  UserEntity({
    required this.id,
    required this.fullname,
    required this.phone,
    required this.email,
    required this.password,
    required this.profile,
    required this.usertype,
  });
}
